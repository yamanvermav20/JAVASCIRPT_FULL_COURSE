//Dynamic typing -> the type of a variable can change during runtime
//Javascript is Dynamically typed

let a = 12;
console.log(a); // 12

a = "Hello, World!";
console.log(a); // "Hello, World!"











//static typing -> the type of a variable is determined at compile time and cannot change during runtime

//In JavaScript, we have dynamic typing, which means that the type of a variable can change during runtime. This allows for more flexibility in coding, but it can also lead to unexpected behavior if not used carefully. For example:
//Use TypeScript or Flow for static typing in JavaScript to catch type-related errors during development.
let x = 10; // x is a number
console.log(x); // 10
x = "Now I'm a string!"; // x is now a string
console.log(x); // "Now I'm a string!"
